class DynamicResponseGenerator:
    """
    Generate dynamic responses by combining templates and context
    """
    
    def __init__(self):
        self.matcher = ConversationalPatternMatcher()
        self.context_memory = {}  # session_id -> context
    
    def generate(self, text: str, session_id: str = "default",
                 lang: str = "ru", context: Optional[Dict] = None) -> str:
        """Generate contextual response"""
        # Get or create session context
        if session_id not in self.context_memory:
            self.context_memory[session_id] = {
                "history": [],
                "topics": [],
                "last_category": None,
                "sentiment_sum": 0.0,
                "turn_count": 0
            }
        
        ctx = self.context_memory[session_id]
        
        # Estimate sentiment from simple keywords
        sentiment = self._estimate_sentiment(text, lang)
        ctx["sentiment_sum"] += sentiment
        ctx["turn_count"] += 1
        
        # Get response
        response, category = self.matcher.get_contextual_response(
            text, lang,
            previous_category=ctx["last_category"],
            sentiment=ctx["sentiment_sum"] / ctx["turn_count"]
        )
        
        # Update context
        ctx["history"].append({"role": "user", "text": text})
        ctx["history"].append({"role": "assistant", "text": response})
        ctx["last_category"] = category
        
        # Keep history limited
        if len(ctx["history"]) > 20:
            ctx["history"] = ctx["history"][-20:]
        
        return response
    
    def _estimate_sentiment(self, text: str, lang: str) -> float:
        """Quick sentiment estimation"""
        text_lower = text.lower()
        
        positive = POSITIVE_WORDS_RU if lang == "ru" else {"good", "great", "thanks", "happy", "love", "nice"}
        negative = NEGATIVE_WORDS_RU if lang == "ru" else {"bad", "hate", "problem", "sad", "angry", "terrible"}
        
        pos_count = sum(1 for word in positive if word in text_lower)
        neg_count = sum(1 for word in negative if word in text_lower)
        
        total = pos_count + neg_count
        if total == 0:
            return 0.0
        
        return (pos_count - neg_count) / total
    
    def clear_session(self, session_id: str):
        """Clear session context"""
        if session_id in self.context_memory:
            del self.context_memory[session_id]


