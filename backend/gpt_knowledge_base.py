class ConversationalPatternMatcher:
    """
    Match user input to conversational patterns
    Uses fuzzy matching and keyword analysis
    """
    
    def __init__(self):
        self.patterns = GPT_CONVERSATIONS
        self._build_index()
    
    def _build_index(self):
        """Build keyword index for faster matching"""
        self.keyword_index = {}
        
        for category, lang_data in self.patterns.items():
            for lang, patterns in lang_data.items():
                for pattern_group in patterns:
                    for keyword in pattern_group["pattern"]:
                        key = (keyword.lower(), lang)
                        if key not in self.keyword_index:
                            self.keyword_index[key] = []
                        self.keyword_index[key].append({
                            "category": category,
                            "responses": pattern_group["responses"]
                        })
    
    def find_response(self, text: str, lang: str = "ru") -> Optional[Tuple[str, str, float]]:
        """
        Find best matching response for text
        Returns: (response, category, confidence)
        """
        text_lower = text.lower()
        words = set(text_lower.split())
        
        best_match = None
        best_score = 0.0
        best_category = "unknown"
        
        # Check each keyword
        for (keyword, key_lang), matches in self.keyword_index.items():
            if key_lang != lang:
                continue
            
            # Check if keyword is in text
            if keyword in text_lower:
                # Calculate score based on match quality
                score = len(keyword) / max(len(text_lower), 1)
                
                # Boost for exact word match
                if keyword in words:
                    score *= 1.5
                
                # Boost for shorter queries (more specific)
                if len(text_lower) < 30:
                    score *= 1.2
                
                if score > best_score:
                    best_score = score
                    best_match = random.choice(matches[0]["responses"])
                    best_category = matches[0]["category"]
        
        if best_match:
            return (best_match, best_category, min(best_score, 1.0))
        
        return None
    
    def get_contextual_response(self, text: str, lang: str = "ru",
                                 previous_category: Optional[str] = None,
                                 sentiment: float = 0.0) -> Tuple[str, str]:
        """
        Get contextually appropriate response
        Considers previous category and sentiment
        """
        result = self.find_response(text, lang)
        
        if result:
            response, category, confidence = result
            
            # Add sentiment-appropriate prefix
            if sentiment > 0.3:
                prefix = random.choice([
                    "–†–∞–¥ –≤–∞—à–µ–º—É –ø–æ–∑–∏—Ç–∏–≤—É! " if lang == "ru" else "Love your positivity! ",
                    "–û—Ç–ª–∏—á–Ω–æ! " if lang == "ru" else "Great! ",
                ])
                response = prefix + response[0].lower() + response[1:]
            elif sentiment < -0.3:
                prefix = random.choice([
                    "–ü–æ–Ω–∏–º–∞—é –≤–∞—à–∏ —á—É–≤—Å—Ç–≤–∞. " if lang == "ru" else "I understand. ",
                    "–°–æ—á—É–≤—Å—Ç–≤—É—é. " if lang == "ru" else "I'm sorry. ",
                ])
                response = prefix + response
            
            return response, category
        
        # Fallback
        if lang == "ru":
            return "–ò–Ω—Ç–µ—Ä–µ—Å–Ω—ã–π –≤–æ–ø—Ä–æ—Å! –†–∞—Å—Å–∫–∞–∂–∏—Ç–µ –ø–æ–¥—Ä–æ–±–Ω–µ–µ?", "unknown"
        return "Interesting question! Tell me more?", "unknown"
    
    def get_all_patterns(self, category: str, lang: str = "ru") -> List[str]:
        """Get all patterns for a category"""
        if category in self.patterns and lang in self.patterns[category]:
            all_keywords = []
            for pattern_group in self.patterns[category][lang]:
                all_keywords.extend(pattern_group["pattern"])
            return all_keywords
        return []


